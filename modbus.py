class ModbusErrors(IntEnum):
    illegal_function = 0x01
    illegal_data_address = 0x02
    illegal_data_value = 0x03
    server_device_failure = 0x04
    acknowledge = 0x05
    server_device_busy = 0x06
    memory_parity_error = 0x08
    gateway_path_unavailable = 0x0A
    gateway_target_device_failed_to_respond = 0x0B


class ModbusBridge:
    def __init__(self, transport):
        self.transport = transport
        self.server = TcpServer(self.transaction)
        self._client: int
        self._command: int

    def run(self):
        self.server.run()

    def crc(self, data: bytes) -> (int, int):
        def calcCRC(byte: int, oldCRC):
            high, low = oldCRC & 0xFF, (oldCRC >> 8) & 0xFF
            uIndex = low ^ byte
            high, low = high ^ srCRCHigh[uIndex], srCRCLow[uIndex]
            CRC = (high << 8 | low)
            return CRC
        CRC = calcCRC(data[0], 0xFFFF)
        for i in range(1, len(data)):
            CRC = calcCRC(data[i], CRC)
        return (CRC // 256), (CRC % 256)

    def check_crc(self, data: bytes) -> bool:
        return self.crc(data[:-2]) == (data[-2], data[-1])

    def error_response(self, error: ModbusErrors) -> bytes:
        return bytes([self._client, self._command & 0x80, error])

    def transaction(self, data: bytes):
        if not self.check_crc(data):
            logging.debug(f"Bad request CRC: {data}")
            return None
        self._client = data[0]
        self._command = data[1]
        self.transport.transmit(data)
        deadline = datetime.now() + timedelta(milliseconds=500)
        while datetime.now() < deadline:
            data = self.transport.receive()
            if len(data) < 4:
                continue
            if data[0] != self._client:
                logging.debug(f"Received bad data: {data}")
                continue
            if not self.check_crc(data):
                logging.debug(f"Bad response CRC: {data}")
                return None
            return data
        logging.debug(f"Response from {self._client} does not received")
        return self.error_response(ModbusErrors.gateway_target_device_failed_to_respond)


class TcpServer:
    def __init__(self, handler, ip="0.0.0.0", port=502):
        self.ip = ip
        self.port = port
        self.transaction_handler = handler

    def handle_connection(self, conn):
        while True:
            request = conn.recv(1024)
            if not request:
                logging.debug("No data received")
                break
            response = self.transaction_handler(request)
            if response is not None:
                conn.send(response)

    def run(self):
        with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as sock:
            try:
                sock.bind((self.ip, self.port))
                logging.info(f"Listening on {self.ip}:{self.port}")
            except:
                logging.critical(f"Can not bind to {self.ip}:{self.port}")
                exit()
            # Only one connection
            sock.listen(1)
            while True:
                conn, addr = sock.accept()
                logging.info(f"Received connection from {addr[0]}:{addr[1]}")
                with conn:
                    self.handle_connection(conn)
                logging.info(f"Connection from {addr[0]}:{addr[1]} closed")